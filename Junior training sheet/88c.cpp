ll n, m;

void input(){
	cin >> n >> m;
}

void solve(){
	ll d = __gcd(n, m);
	d = n*m/d;
	ll x = d/n, y = d/m;
	if(n > m)y--;
	else if(m > n)x--;
	if(x > y)cout << "Dasha";
	else if(x < y)cout << "Masha";
	else cout << "Equal";
}

